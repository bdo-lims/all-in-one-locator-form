export const hotelList = []

  for (var hotel in hotelDetails) {
    var hotelItem = hotelDetails[hotel];
    hotelList.push({
      "value": hotelItem.tradeName ? hotelItem.tradeName : hotelItem.address,
      "label": hotelItem.tradeName + " , " + hotelItem.address + " , " + hotelItem.district ,
      "address": hotelItem.address + " , " + hotelItem.district
    })
  }

const containsName = (hotelName) => {
  return !!hotelList.find((hotel) => hotel.value === hotelName);
}

export const getHotelAddress = (hotelName) => {
  if (hotelName) {
    if (containsName(hotelName)) {
      return hotelList.find((hotel) => hotel.value === hotelName).address;
    }
  } else {
    return "";
  }
}
