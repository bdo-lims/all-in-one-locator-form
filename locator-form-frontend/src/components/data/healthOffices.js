export const LocalityList = []

for (var healthOffice in healthOfficesList) {
  var healthOfficeItem = healthOfficesList[healthOffice];
  healthOfficeItem.localities.forEach(locality => LocalityList.push({ "value": locality, "label": locality }));
}

const containsLocality = (locality) => {
  return !!healthOfficesList.find((healthOffice) => healthOffice.localities.includes(locality));
}

export const getHealthOffice = (locality) => {
  if (locality) {
    if (containsLocality(locality)) {
      return healthOfficesList.find((e) => e.localities.includes(locality)).value;
    }
  } else {
    return "";
  }
}